   /**
    * Evento que valida la cantidad a ingresar en el cuadro de dialog.
    * @param evt Evento del tipo action
    */
    private void campoTextoCantidadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_campoTextoCantidadActionPerformed
        String cantidad = campoTextoCantidad.getText();
        String msjError = "";
        if (validadores.cadenaVacia(cantidad)) {
            msjError = "Falt√≥ la cantidad";
        } else if (!validadores.validaEntero(cantidad)) {
            msjError = "Solo digitos enteros";
        }
        if (!msjError.equals("")) {
            JOptionPane.showMessageDialog(this, msjError,
                    "Error de entrada.",
                    JOptionPane.ERROR_MESSAGE);
            campoTextoCantidad.requestFocus();
        }

    }//GEN-LAST:event_campoTextoCantidadActionPerformed
