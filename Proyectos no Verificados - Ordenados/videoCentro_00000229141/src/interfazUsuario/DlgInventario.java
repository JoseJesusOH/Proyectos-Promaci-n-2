  /**
   * Metodo oyente del boton acetar,Valida los campos de entrada, y este establece un nuevo objeto del tipo de videojuegoED.
   * @param evt  Evento al que escucha
   */
    private void botonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAceptarActionPerformed

        if (validadores.cadenaVacia(campoTextoCantidad.getText())) {
            JOptionPane.showMessageDialog(this, "Cantidad Vacia");
        } else if (!validadores.validaEntero(campoTextoCantidad.getText())) {
            JOptionPane.showMessageDialog(this, "Solo enteros");
        } else if (operacion == ConstantesGUI.AGREGAR) {
            int auxiliar = videojuegoED.getDisponibilidad() + Integer.valueOf(campoTextoCantidad.getText());
            int auxiliar2 = videojuegoED.getExistencia() + Integer.valueOf(campoTextoCantidad.getText());
            videojuegoED.setVideojuego((Videojuego) cajaCombinadaVideojuegos.getSelectedItem());
            videojuegoED.setDisponibilidad(auxiliar);
            videojuegoED.setExistencia(auxiliar2);
            respuesta.delete(0, respuesta.length());
            respuesta.append(ConstantesGUI.ACEPTAR);
            dispose();
        }


    }//GEN-LAST:event_botonAceptarActionPerformed

